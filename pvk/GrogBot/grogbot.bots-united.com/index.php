							<td>	<p class="header">About</p>
	
	<p class="text"><b>GrogBot</b> is an enhanced version of HPB-bot 3.0 designed to provide decent bot support for a Half-Life 1 modification know as <a href="http://www.pvkii.com">Pirates, Vikings and Knights</a>. The mod itself has been discontinued, but the community around it still keeps the mod alive. The latest version of PVK (2.31) comes with GrogBot 1.0 and PVK Bugfixer included.
	<br><br>
	A new and improved incarnation of PVK is now also available for Half-Life 2. <a href="http://www.pvkii.com">Pirates, Vikings and Knights II</a> brings many of the classic features of Pirates, Vikings and Knights to the Source engine along with an improved combat system and tons of other new features. The development still continues and PVKII grows bigger and better every day. Although it doesn't have bots yet, it's a lot of fun to play!
	<br><br>
	GrogBot is finished and the final version (1.1) was released in 2005. I'm now working as a coder on PVKII so maybe one day GrogBot will return on the new engine :)
	<br><br>
	<b>GrogBot features include:</b><br>
	- Works on most of the official maps out of the box. (waypoints included)<br>
	- Bots are able to complete map objectives in all of the PVK gamemodes. (booty, booty tag, territory and pillage)<br>
	- Intelligent combat behaviour with both melee and ranged weapons.<br>
	- MetaMod plugin and direct DLL hook versions available. <br>
	<br>
	
