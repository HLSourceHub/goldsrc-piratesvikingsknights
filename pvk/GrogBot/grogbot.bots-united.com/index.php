	<br><br>
	<b>PVK Bugfixer</b> is a MetaMod plugin that fixes many glitches present in the latest official PVK release. As development on PVK ceased, these bugs stayed and were never fixed by official patches.
	
	<br><br>
	<b>Some of the glitches that PVK Bugfixer fixes:</b><br>
	- Chest duplicating<br>
	- Skin hacking<br>
	- Alt-walking (increased speed)<br>
	- Bugs with spear charge<br>
